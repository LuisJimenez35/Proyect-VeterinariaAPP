    private void btnDescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescuentoActionPerformed
        int Subtot;
        
        
        Subtot = Integer.parseInt(TxtSubtotal.getText());
        double dcto;
        if(comboMetodo.getSelectedItem().equals("<Seleccionar>")){
           JOptionPane.showMessageDialog(null,"Seleccione una forma de pago");
           return;
        }else{
        }
          
        if(comboMetodo.getSelectedItem().equals("Efectivo")){
            dcto = Subtot * 0.95;
            txtTotal.setText(Integer.toString((int)dcto));
            
        }else if (comboMetodo.getSelectedItem().equals("Tarjeta")){ 
            txtTotal.setText(Integer.toString((int)Subtot));         
        }     
    }//GEN-LAST:event_btnDescuentoActionPerformed

