    public void cargardatos(){
        modelo.setRowCount(0);
        
        int Subtot = 0;
        
        
        for (int i = 0; i < ArrayNuevos.nuevosCarritodeCompras.size(); i++) {

           modelo.addRow(new Object[]{
           ArrayNuevos.nuevosCarritodeCompras.get(i).getNombre(),
           ArrayNuevos.nuevosCarritodeCompras.get(i).getValor(),
           ArrayNuevos.nuevosCarritodeCompras.get(i).getPrecio(),
           ArrayNuevos.nuevosCarritodeCompras.get(i).getCantidad()
                   
           });
           
           Subtot = Subtot + ArrayNuevos.nuevosCarritodeCompras.get(i).getPrecio();
        }
        
        TxtSubtotal.setText(Integer.toString(Subtot));
    }
    

