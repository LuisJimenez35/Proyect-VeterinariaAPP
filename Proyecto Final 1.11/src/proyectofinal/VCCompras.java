    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        if (tablaCarrito.getSelectedRow() != -1) {
            int index = tablaCarrito.getSelectedRow();
            int precioResta = ArrayNuevos.nuevosCarritodeCompras.get(index).getPrecio();
            int seteoPrecio = Integer.parseInt(TxtSubtotal.getText());
            int total =seteoPrecio - precioResta;
            TxtSubtotal.setText(String.valueOf(total));
            txtTotal.setText(String.valueOf(total));
            modelo.removeRow(index);
            ArrayNuevos.nuevosCarritodeCompras.remove(index);
                    
        }
        else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un objeto de la tabla");
        }
        
        
        
    }//GEN-LAST:event_btndeleteActionPerformed

    
