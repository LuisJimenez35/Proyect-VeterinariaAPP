    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        String usuario = txtUsuario.getText();
        String pass = txtPass.getText();
       
        boolean encontrar = true;
        for (Usuarios i : IniciarDatos.nuestrosUsuarios) {
            if (i.getUsuario().equals(usuario) && i.getClave().equals(pass)) {
                encontrar = true;
                IniciarDatos.rolUsuario =  i.getRol();
                break;
            }else{
                encontrar = false;
            }
        }
                          
        if (encontrar) {
            VPrincipal pln = new VPrincipal();
            pln.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se encontro el usuario en el sistema");
        }
        if (IniciarDatos.rolUsuario.equals("Administrador")){
            Botones pln = new Botones();
            pln.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jLabel5MouseClicked

